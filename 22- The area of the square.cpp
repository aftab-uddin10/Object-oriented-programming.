#include <iostream>
using namespace std;
class Square {
private:
    double Length;

public:

    Square(){
        Length=1.0;
    }

    Square(double length) {
        Length=length;
    }

    double calculate() {
        return Length * Length;
    }
};

int main() {
    Square square1;
    Square square2(5.0);

    cout << "Area of square1: " << square1.calculate() << endl;
    cout << "Area of square2: " << square2.calculate() << endl;

    return 0;
}
